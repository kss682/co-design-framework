BASE_JSON='{
    "nodes": [
        { "id": 100, "type": "ES" },
        { "id": 101, "type": "ES" },
        { "id": 1, "type": "NN" }
    ],

    "links": [
        {
            "src": 100,
            "dst": 1
        },
        {
            "src": 1,
            "dst": 101
        }
    ],

    "streams": [
        {
            "id": 1,
            "src": 100,
            "dst": 101,
            "plant_id": 1,
            "type": "tt",
            "release_time": 0,
            "period": 0.020,
            "deadline": 0.020
        }
    ],

    "modes": {
        "1": { "name": "moving1", "streams": [1], "schedule_id": 1 }
    },

    "schedules": {
        "1": {
            "pattern": [1, 0, 0],
            "hit_delay": 0.0019,
            "miss_delay": 0.021
        }
    },

    "mode_switch":[
        [1, 0]
    ],

    "link_delay": [
        { "prob": 0.95, "delay": 0.005},
        { "prob": 0.95, "delay": 0.005}
    ],

    "preconditions": {
        "network_nodes": {
