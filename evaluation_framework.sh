        }
    },

    "delivery_constraints": [
        {
            "mode_id": 1,
            "stream_id": 1,
            "min_packets": 8,
            "window_size": 3
        }
    ]    
}'

# Results file with timestamp
TIMESTAMP=$(date +"%Y%m%d_%H%M%S")
RESULTS_FILE="benchmark/pattern_results_${TIMESTAMP}.csv"

# Create CSV header
echo "consecutive_misses,pattern,status,max_theta" > "$RESULTS_FILE"

# Create a temporary directory for config files
TEMP_DIR=$(mktemp -d)
CONFIG_FILE="$TEMP_DIR/config.json"
PENDULUM_OUTPUT="simulation_results/plant1_trace_states.csv"

# Simulation input file (generated by main.py)
SIM_TRACE_FILE="simulation_results/plant1_trace.csv"

echo "Running experiments with patterns up to 20 consecutive misses..."
echo "Results will be saved to: $RESULTS_FILE"
echo "=============================================="

# Function to extract max absolute theta from CSV with columns t,p,v,theta,omega
extract_max_theta() {
    local csv_file="$1"
    if [ -f "$csv_file" ]; then
        # Skip header, get theta column (4th), compute max absolute value
        tail -n +2 "$csv_file" | \
            awk -F',' '{
                if ($4 != "" && $4 ~ /^-?[0-9]/) {
                    val = ($4 < 0) ? -$4 : $4
                    if (val > max) max = val
                }
            } END {
                if (max != "") printf "%.10f", max
                else print "N/A"
            }'
    else
        echo "N/A"
    fi
}

# Generate patterns from [1] to [1, 0, 0, ..., 0] (up to 20 zeros)
for num_misses in $(seq 0 10); do
    # Build the pattern string: [1] for 0 misses, [1, 0] for 1 miss, etc.
    if [ $num_misses -eq 0 ]; then
        pattern="[1]"
    else
        zeros=""
        for i in $(seq 1 $num_misses); do
            zeros="$zeros, 0"
        done
        pattern="[1$zeros]"
    fi
    
    echo ""
    echo "[$num_misses/20] Running with $num_misses consecutive misses: $pattern"
    echo "----------------------------------------------"
    
    # Clean up previous simulation results
    rm -rf simulation_results/*
    
    # Use jq to update the pattern in the JSON
    echo "$BASE_JSON" | jq --argjson pat "$pattern" '.schedules."1".pattern = $pat' > "$CONFIG_FILE"
    
    # Run main.py and capture output
    main_output=$(python main.py -f "$CONFIG_FILE" -s Delay -t 20 2>&1)
    main_exit_code=$?
    
    # Determine status (violated or satisfied)
    if echo "$main_output" | grep -qi "violated"; then
        status="violated"
    elif echo "$main_output" | grep -qi "satisfied"; then
        status="satisfied"
    else
        status="unknown"
    fi
    
    if [ $main_exit_code -eq 0 ]; then
        echo "  ✓ main.py completed - Status: $status"
    else
        echo "  ✗ main.py failed with exit code $main_exit_code"
    fi
    
    # Run pendulum simulator and capture output to file
    python pendulum_simulator.py -f "$SIM_TRACE_FILE"
    sim_exit_code=$?
    
    # Extract max absolute theta from the CSV output
    max_theta=$(extract_max_theta "$PENDULUM_OUTPUT")
    
    if [ $sim_exit_code -eq 0 ]; then
        echo "  ✓ Pendulum simulator completed - Max |theta|: $max_theta rad"
    else
        echo "  ✗ Pendulum simulator failed with exit code $sim_exit_code"
        max_theta="N/A"
    fi
    
    # Append to CSV
    echo "$num_misses,\"$pattern\",$status,$max_theta" >> "$RESULTS_FILE"
done

# Cleanup
rm -rf "$TEMP_DIR"

echo ""
echo "=============================================="
echo "All patterns completed!"
echo "Results saved to: $RESULTS_FILE"
echo ""
echo "=== RESULTS SUMMARY ==="
echo ""
printf "%-6s %-45s %-12s %s\n" "Misses" "Pattern" "Status" "Max |theta|"
printf "%-6s %-45s %-12s %s\n" "------" "-------" "------" "-----------"
tail -n +2 "$RESULTS_FILE" | while IFS=',' read -r misses pattern status theta; do
    # Remove quotes from pattern
    pattern=$(echo "$pattern" | tr -d '"')
    printf "%-6s %-45s %-12s %s\n" "$misses" "$pattern" "$status" "$theta"
done